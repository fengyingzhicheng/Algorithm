class K个一组翻转链表 {
    public static void main(String[] args) {
        Solution solution = new K个一组翻转链表().new Solution();

    }
//leetcode submit region begin(Prohibit modification and deletion)

    //  Definition for singly-linked list.
    public class ListNode {
        int val;
        ListNode next;

        ListNode() {
        }

        ListNode(int val) {
            this.val = val;
        }

        ListNode(int val, ListNode next) {
            this.val = val;
            this.next = next;
        }
    }

    class Solution {
        public ListNode reverseKGroup(ListNode head, int k) {
            // base case 最小递归情况 head前进k个不到为 null
            ListNode right = head;
            for (int i = 0; i < k - 1 && right != null; i++) {
                right = right.next;
            }
            if (right == null) {
                //如果不能被k 整除
                return head;
            }

            ListNode last = reverseKGroup(right.next, k);

            ListNode data = reverseN3(head, k);

            head.next=last;
            return data;
        }

        ListNode successor = null; // 后驱节点

        // 反转以 head 为起点的 n 个节点，返回新的头结点
        ListNode reverseN3(ListNode head, int n) {
            if (n == 1) {
                // 记录第 n + 1 个节点
                successor = head.next;
                return head;
            }
            // 以 head.next 为起点，需要反转前 n - 1 个节点
            ListNode last = reverseN3(head.next, n - 1);

            head.next.next = head;
            // 让反转之后的 head 节点和后面的节点连起来
            head.next = successor;
            return last;
        }
    }
//leetcode submit region end(Prohibit modification and deletion)

}
